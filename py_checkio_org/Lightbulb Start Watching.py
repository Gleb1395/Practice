from datetime import datetime
from typing import List, Optional


def sum_light(els: List[datetime], start_watching: Optional[datetime] = None) -> int:
    index = 0
    sum_time = 0
    if not start_watching:
        for x in range(len(els) // 2):
            turn_off_time = els[index + 1]
            turn_on_time = els[index]
            difference_time = turn_off_time - turn_on_time
            sum_time += difference_time.total_seconds()
            index += 2
        return int(sum_time)
    else:
        for x in range(len(els) // 2):
            turn_off_time = els[index + 1]
            turn_on_time = els[index]
            if start_watching >= turn_on_time and start_watching < turn_off_time:
                difference_time = turn_off_time - start_watching
                sum_time += difference_time.total_seconds()
                index += 2
            elif start_watching == turn_off_time:
                difference_time = 0
                sum_time += difference_time
                index += 2
            else:
                pass
                index += 2

        return int(sum_time)


assert (
        sum_light(
            els=[
                datetime(2015, 1, 12, 10, 0, 0),
                datetime(2015, 1, 12, 10, 0, 10),
            ],
            start_watching=datetime(2015, 1, 12, 10, 0, 5),
        )
        == 5
)

